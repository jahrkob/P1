# Precompute HW table 0..255
HW = np.array([bin(x).count("1") for x in range(256)], dtype=np.float32)


# ================================
# Pearson correlation
# ================================
def pearson_corr(x, y):
    """
    x, y: 1D numpy arrays of same length
    """
    x = x.astype(np.float64)
    y = y.astype(np.float64)
    x = x - x.mean()
    y = y - y.mean()
    num = np.dot(x, y)
    den = np.sqrt(np.dot(x, x) * np.dot(y, y))
    if den == 0:
        return 0.0
    return num / den


# ================================
# CPA attack
# ================================
recovered_key = np.zeros(16, dtype=np.uint8)

for byte_idx in range(16):
    # Known point of interest for this byte (as in the generator):
    # poi = 10 + 10 * byte_idx  -> 10, 20, ..., 160
    poi = 10 + 10 * byte_idx

    if poi >= trace_length:
        raise ValueError(f"POI {poi} out of range for trace length {trace_length}")

    samples = traces[:, poi]  # all traces at this time sample

    best_corr = -1.0
    best_guess = 0

    print(f"\n[+] Attacking subkey byte {byte_idx} at POI {poi}")

    # Attack all 256 key candidates for this byte
    for kguess in range(256):
        # plaintext byte for this position across all traces
        pbytes = textins[:, byte_idx]

        # SBox[plaintext ^ key_guess]
        inter = SBOX[pbytes ^ kguess]

        # HW of that intermediate value
        hyp = HW[inter]

        # Correlate hypothetical leakage with measured leakage
        corr = abs(pearson_corr(hyp, samples))

        if corr > best_corr:
            best_corr = corr
            best_guess = kguess

    recovered_key[byte_idx] = best_guess
    print(f"    -> best guess: 0x{best_guess:02x}  (|corr| = {best_corr:.5f})")

